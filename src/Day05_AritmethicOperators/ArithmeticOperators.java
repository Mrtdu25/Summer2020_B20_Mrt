package Day05_AritmethicOperators;

public class ArithmeticOperators {
    public static void main(String[] args) {

        System.out.println(20+3-2+1); //example for + and -//23-2+1 ==>21+1=22

        System.out.println(2+3-1*4);// 2+3-4 ==> 5-4=1   ////example for *

        System.out.println(10.0/3);////example for /  //int/int=int  but double/int=double
        System.out.println(10/3);  //for right result you need to make one number decimal.
        System.out.println(10/3.0);// int/double=double

        System.out.println(10.0%4); //
        System.out.println(30%8);

        System.out.println(99%4);





    }




}
