package Day06_ComparisonOperators;

public class Swap1 {
    public static void main(String[] args) {

        int a = 10;
        int b = 15;
        int c =b; //    c =15

        b =a; // b became 10 like a
        a =c;// c became 15 instead of b  we call this SWAP


        System.out.println(a);
        System.out.println(b);






    }






}
