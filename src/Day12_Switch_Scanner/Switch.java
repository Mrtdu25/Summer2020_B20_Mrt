package Day12_Switch_Scanner;

public class Switch {
    public static void main(String[] args) {

        int a=3;

        switch (a) {
            case 1:
                System.out.println("one");
                break;

            case 5:
                System.out.println("five");
                break;
            case  3:
                System.out.println("Three");
            break;

            default:
                System.out.println("Invalid Case");
              break;

        }







    }
}
