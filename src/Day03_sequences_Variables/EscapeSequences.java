package Day03_sequences_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone \nMy name is Cybertek "); //Escape Sequences (\n)

        System.out.println(" \tI am in Virginia \n"); // Paragraph space (\t)

        System.out.println("\n\n\t\t\tI love Programming ");// we can do more than 1 line


        System.out.println("\\"); // to print back slash \
        System.out.println("/"); // to print front slash you need only /

        System.out.println("\\\\"); //you can do as many as you want adding more double back slash
        System.out.println("/////"); // you can do as many as you want adding mot single front slash

        System.out.println("My name is\'Murat\'"); //How to make single Quote 'Murat'
        System.out.println("My name is \"Murat\"");// How to make Double Quote "Murat"

        System.out.println("Your name is \'Duaa\'");// Single line note
        System.out.println("Your name is \"Duaa\"");// Another single line note

        /*
        1-you also take more than one line note
        2-example 2
        3-example 3

         */





    }




}
